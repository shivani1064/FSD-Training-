//while-loop
//-->it will iterates set of instruction untill
//condition become fale

// syntax

/*
  while(condition)
  {
   set-of-statement
  }

*/

let i = 1
while(i<=5)
{
    console.log(i);
    i++
}
//1 2 3 4 5


//do-while-loop
//it will execute first and then checks condition
//by default one iteration will be present

/* 
  syntax:-
       do{
          set-of-instruction
        }while(condition)

*/

let a = 1
do{
    console.log(a);
    a++
  }while(a<=5)



//for-loop

/*
 for(initialization; condition; increment/decrement)
 {
   set-of-instruction
 }
*/

for(let i=1; i<=5; i++)
{
    console.log(i);
}
